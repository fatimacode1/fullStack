//9. Loose vs Strict Equality
//Loose Equality (==)
//Compares value only (converts types if needed)
// 5 == "5" //true
//true == 1 //true
// 0 == false //true

//Strict Equality
//Compares both value AND type (no conversion)
// 5 === "5" //false
// true === 1 //false
//5 === 5 //true

